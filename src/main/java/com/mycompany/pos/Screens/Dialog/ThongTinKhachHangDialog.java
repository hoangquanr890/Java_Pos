    private void setFilter() {
        PlainDocument doc = (PlainDocument) txtPhone.getDocument();
        doc.setDocumentFilter(new MyIntFilter());
    }
    
    public void clearData() {
        _customer = null;
        txtFirstName.setText("");
        txtLastName.setText("");
        txtPhone.setText("");
        txtBirthday.setDate(null);
    }
    
    public void setStatus(Status status){
        _status = status;
        switch (status) {
            case ADD:
                jButton1.setVisible(false);
                jButton2.setVisible(true);
                jButton3.setVisible(true);
                
                jButton2.setText("Thêm");
                jButton3.setText("Trở lại");
                break;
            case UPDATE:
                jButton1.setVisible(true);
                jButton2.setVisible(true);
                jButton3.setVisible(true);
                
                jButton2.setText("Xoá");
                jButton2.setText("Chỉnh sửa");
                jButton3.setText("Trở lại");
                break;
                
            default:
                break;
        } 
    }
    
    private void delete() {
    
    }

    private void update(){
        long id = _customer.getIdCustomer();
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String phone = txtPhone.getText();
        Date dob = txtBirthday.getDate();
        
        Customer customer = new Customer();
        customer.setIdCustomer(id);
        customer.setFirstName(firstName);
        customer.setLastName(lastName);
        customer.setPhone(phone);
        customer.setBirthday(dob);
        
        cs.save(customer);
        this.dispose();
    }
    
    private void add(){
        String firstName = txtFirstName.getText();
        String lastName = txtLastName.getText();
        String phone = txtPhone.getText();
        Date dob = txtBirthday.getDate();
        
        Customer customer = new Customer();
        customer.setFirstName(firstName);
        customer.setLastName(lastName);
        customer.setPhone(phone);
        customer.setBirthday(dob);
        
        cs.save(customer);
        this.dispose();
    }

    public void setCustomer(Customer customer) {
        _customer = customer;
        
        txtFirstName.setText(customer.getFirstName());
        txtLastName.setText(customer.getLastName());
        txtPhone.setText(customer.getPhone());
        txtBirthday.setDate(customer.getBirthday()); 
        
    }
