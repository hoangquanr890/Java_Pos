    public boolean checkUser() {
        List<User> listUser = _userService.findAll();
        if (listUser.size() <= 0){
            User user = new User();
            user.setUsername("admin");
            user.setPassword("admin");
            _userService.save(user);
        }
        return true;
    }
    
